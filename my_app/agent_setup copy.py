def initialize_sql_agent(db_uri, llm_choice, user_id: str, line_id: str):
    try:
        db_instance = SQLDatabase.from_uri(db_uri)
    except Exception as e:
        print(f"ERROR: Failed to initialize SQLDatabase from URI '{db_uri}': {e}")
        return None
        
    llm = None
    try:
        if "gemini-2.5-flash" in llm_choice:
            google_api_key = os.getenv("GOOGLE_API_KEY")
            if not google_api_key:
                print(f"ERROR: ‡πÑ‡∏°‡πà‡∏û‡∏ö GOOGLE_API_KEY ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö {llm_choice}. ‡πÇ‡∏õ‡∏£‡∏î‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡πÉ‡∏ô‡πÑ‡∏ü‡∏•‡πå .env.")
                return None
            llm = ChatGoogleGenerativeAI(model=llm_choice, temperature=0, google_api_key=google_api_key)
        else:
            print("ERROR: Model ‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á.")
            return None
    except Exception as e:
        print(f"Error initializing LLM ({llm_choice}): {e}")
        return None
    
    if llm is None:
        return None 
    
    try:
        toolkit = SQLDatabaseToolkit(db=db_instance, llm=llm)

        # 1. ‡πÇ‡∏´‡∏•‡∏î‡∏õ‡∏£‡∏∞‡∏ß‡∏±‡∏ï‡∏¥‡∏Å‡∏≤‡∏£‡∏™‡∏ô‡∏ó‡∏ô‡∏≤
        chat_history = load_history_from_db(user_id, line_id) 

        # 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á Memory
        memory = ConversationBufferMemory(
            memory_key="chat_history", 
            return_messages=True,
            chat_memory=chat_history  # inject ‡∏õ‡∏£‡∏∞‡∏ß‡∏±‡∏ï‡∏¥
        )
        print("=== DEBUG MEMORY ===")
        print(memory.load_memory_variables({}))
        print("====================")

        # 4. ‡∏™‡∏£‡πâ‡∏≤‡∏á sql agent (agent object ‡πÄ‡∏â‡∏¢ ‡πÜ)
        sql_agent = create_sql_agent(
            llm=llm,
            toolkit=SQLDatabaseToolkit(db=db_instance, llm=llm),
            verbose=True,
            agent_type="openai-tools",
            prefix=AGENT_PREFIX,
            handle_parsing_errors=True,
            return_intermediate_steps=True
        )

        # 5. ‡∏´‡πà‡∏≠‡∏î‡πâ‡∏ß‡∏¢ AgentExecutor + memory
        agent_executor = AgentExecutor.from_agent_and_tools(
            agent=sql_agent.agent,
            tools=sql_agent.tools,
            memory=memory,
            verbose=True,
            handle_parsing_errors=True
        )
        return agent_executor  # üü¢ ‡∏ï‡πâ‡∏≠‡∏á return ‡∏ï‡∏±‡∏ß‡∏ô‡∏µ‡πâ ‡πÑ‡∏°‡πà‡πÉ‡∏ä‡πà sql_agent
    except Exception as e:
        print(f"ERROR: Failed to initialize agent: {e}")
        return None
